using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace pemrogramanlatihan
{
    class Calculator
    {
        public static int penjumlah(int a, int b)
        {
            return a + b;
        }
        public static int penjumlah (int a, int b, int c)
        { 
            return a + b + c;
        }
        public static int pengurangan (int a, int b)
        {
            return a - b;
        }
        public static int perkalian(int a, int b)
        {
            return a * b;
        }
    }
}
